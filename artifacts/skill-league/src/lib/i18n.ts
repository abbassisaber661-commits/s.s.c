// src/lib/i18n.ts
export type Language = 'en' | 'ar' | 'fr' | 'es' | 'de' | 'pt' | 'tr' | 'hi' | 'zh' | 'ru';

export const LANGUAGES: { code: Language; native: string; label: string; rtl?: boolean }[] = [
  { code: 'en', native: 'English',    label: 'English'    },
  { code: 'ar', native: 'العربية',    label: 'Arabic',    rtl: true },
  { code: 'fr', native: 'Français',   label: 'French'     },
  { code: 'es', native: 'Español',    label: 'Spanish'    },
  { code: 'de', native: 'Deutsch',    label: 'German'     },
  { code: 'pt', native: 'Português',  label: 'Portuguese' },
  { code: 'tr', native: 'Türkçe',     label: 'Turkish'    },
  { code: 'hi', native: 'हिन्दी',      label: 'Hindi'      },
  { code: 'zh', native: '中文',        label: 'Chinese'    },
  { code: 'ru', native: 'Русский',    label: 'Russian'    },
];

export const SUPPORTED_CODES = LANGUAGES.map(l => l.code);
export function isRTL(lang: Language) { return LANGUAGES.find(l => l.code === lang)?.rtl ?? false; }

export function detectBrowserLanguage(): Language {
  const b = navigator.language.split('-')[0];
  return SUPPORTED_CODES.includes(b as Language) ? (b as Language) : 'en';
}

