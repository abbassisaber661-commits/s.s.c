export type TranslationKey = string;

function resolve(obj: Record<string, unknown>, key: string): string | undefined {
  if (key in obj) return obj[key] as string | undefined;
  const parts = key.split('.');
  let cur: unknown = obj;
  for (const part of parts) {
    if (cur == null || typeof cur !== 'object') return undefined;
    cur = (cur as Record<string, unknown>)[part];
  }
  return typeof cur === 'string' ? cur : undefined;
}

export function t(lang: Language, key: TranslationKey): string {
  return resolve(T[lang] as Record<string, unknown>, key)
    ?? resolve(T['en'] as Record<string, unknown>, key)
    ?? key;
}

export function useT(lang: Language) {
  return (key: TranslationKey) => t(lang, key);
}

export { T as translations };

export function getTranslation(lang: Language, key: TranslationKey): string {
  return t(lang, key);
}